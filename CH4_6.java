public class CH4_6 {
    public static void main(String[] args) {
        int a=7,b=13;
        System.out.printf("%d & %d=%d\n", a,b,a&b);
        System.out.printf("%d | %d=%d\n", a,b,a|b);
        System.out.printf("!%d=%d\n", a,~a);
        System.out.printf("%d ^ %d=%d\n", a,b,a^b);
        System.out.printf("%d >> 2=%d\n", a,a>>2);
        System.out.printf("%d << 2=%d\n", a,a<<2);
    }
}
