public class CH3_3 {
    public static void main(String[] args) {
        byte bin=0b1111;  //以二進為表示的整數
        short oct=02001;  //以八進為表示的整數
        int hext1=0xff12;  //以十六進為表示的整數
        long hext2=0x12345ffeL;  //以十六進為表示的長整數

        System.out.println("bin= "+bin);
        System.out.println("oct= "+oct);
        System.out.println("hext1= "+hext1);
        System.out.println("hext2= "+hext2);
    }
}
